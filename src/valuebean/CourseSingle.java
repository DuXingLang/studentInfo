package valuebean;

public class CourseSingle {
	String courseName;
	String courseSta;
	String courseAddr;
	String courseWeek;
	int courseStart;
	int courseCount;
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseSta() {
		return courseSta;
	}
	public void setCourseSta(String courseSta) {
		this.courseSta = courseSta;
	}
	public String getCourseAddr() {
		return courseAddr;
	}
	public void setCourseAddr(String courseAddr) {
		this.courseAddr = courseAddr;
	}
	public String getCourseWeek() {
		return courseWeek;
	}
	public void setCourseWeek(String courseWeek) {
		this.courseWeek = courseWeek;
	}
	public int getCourseStart() {
		return courseStart;
	}
	public void setCourseStart(int courseStart) {
		this.courseStart = courseStart;
	}
	public int getCourseCount() {
		return courseCount;
	}
	public void setCourseCount(int courseCount) {
		this.courseCount = courseCount;
	}
}

package valuebean;

public class Course {
	String courseId;
	String courseName;
	String courseSta;
	String staName;
	String courseTime;
	int courseCredit;
	String courseAddr;
	int courseType;
	int courseTerm;
	String typeName;
	int courseCount;
	public String getCourseId() {
		return courseId;
	}
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseSta() {
		return courseSta;
	}
	public void setCourseSta(String courseSta) {
		this.courseSta = courseSta;
	}
	public String getStaName() {
		return staName;
	}
	public void setStaName(String staName) {
		this.staName = staName;
	}
	public String getCourseTime() {
		return courseTime;
	}
	public void setCourseTime(String courseTime) {
		this.courseTime = courseTime;
	}
	public int getCourseCredit() {
		return courseCredit;
	}
	public void setCourseCredit(int courseCredit) {
		this.courseCredit = courseCredit;
	}
	public String getCourseAddr() {
		return courseAddr;
	}
	public void setCourseAddr(String courseAddr) {
		this.courseAddr = courseAddr;
	}
	public int getCourseType() {
		return courseType;
	}
	public void setCourseType(int courseType) {
		this.courseType = courseType;
	}
	public int getCourseTerm() {
		return courseTerm;
	}
	public void setCourseTerm(int courseTerm) {
		this.courseTerm = courseTerm;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	public int getCourseCount() {
		return courseCount;
	}
	public void setCourseCount(int courseCount) {
		this.courseCount = courseCount;
	}
}
